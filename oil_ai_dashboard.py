# Combined core dataset
merged_df = pd.merge(wti_df, brent_eu_df, on='ds', how='outer')
merged_df = pd.merge(merged_df, brent_sg_df, on='ds', how='outer')
merged_df = merged_df.sort_values('ds').reset_index(drop=True)

# -----------------------------------------------------------------
# 3. INTERACTIVE DASHBOARD CORE LOGIC
# -----------------------------------------------------------------
def generate_forecast_dashboard(market_choice, prediction_days):
    column_map = {
        "US Market (WTI Price)": "WTI_Price",
        "Global Market (Europe Brent)": "Brent_Europe_Price",
        "Asian Market (Singapore Brent)": "Brent_Singapore_Price"
    }

    target_col = column_map[market_choice]
    prophet_df = merged_df[['ds', target_col]].dropna()
    prophet_df.columns = ['ds', 'y']

    if prophet_df.empty:
        return go.Figure().update_layout(title="No data found for this selection.")

    # Train model dynamically
    model = Prophet(daily_seasonality=False, weekly_seasonality=True, yearly_seasonality=False)
    model.fit(prophet_df)

    # Forecast out based on slider selection
    future = model.make_future_dataframe(periods=int(prediction_days), freq='D')
    forecast = model.predict(future)

    # Create interactive Plotly figure
    fig = go.Figure()

    # 1. Shaded Uncertainty Interval
    fig.add_trace(go.Scatter(
        x=pd.concat([forecast['ds'], forecast['ds'].iloc[::-1]]),
        y=pd.concat([forecast['yhat_upper'], forecast['yhat_lower'].iloc[::-1]]),
        fill='toself',
        fillcolor='rgba(0, 123, 255, 0.15)',
        line=dict(color='rgba(255,255,255,0)'),
        hoverinfo="skip",
        name='AI Uncertainty Bounds'
    ))

    # 2. Historical Actual Data Points
    fig.add_trace(go.Scatter(
        x=prophet_df['ds'], y=prophet_df['y'],
        mode='markers',
        marker=dict(color='black', size=4),
        name='Historical Real Price'
    ))

    # 3. AI Target Prediction Trend Line
    fig.add_trace(go.Scatter(
        x=forecast['ds'], y=forecast['yhat'],
        mode='lines',
        line=dict(color='#007bff', width=2),
        name='AI Price Forecast'
    ))

    fig.update_layout(
        title=f"AI Forecast Trend for {market_choice}",
        xaxis_title="Date",
        yaxis_title="Price (USD/BBL)",
        template="plotly_white",
        hovermode="x unified",
        legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1)
    )

    return fig

# -----------------------------------------------------------------
# 4. LAUNCH WEB APP LAYOUT
# -----------------------------------------------------------------
print("🚀 Initializing Dashboard UI...")
app = gr.Interface(
    fn=generate_forecast_dashboard,
    inputs=[
        gr.Dropdown(
            choices=["Global Market (Europe Brent)", "US Market (WTI Price)", "Asian Market (Singapore Brent)"],
            value="Global Market (Europe Brent)",
            label="Select Oil Benchmark Market"
        ),
        gr.Slider(
            minimum=7,
            maximum=90,
            value=30,
            step=1,
            label="Prediction Window (Days into the Future)"
        )
    ],
    outputs=gr.Plot(label="Interactive Forecast Chart"),
    title="🛢️ Interactive AI Oil Price Forecasting Dashboard",
    description="Select an oil benchmark asset and dynamically adjust the machine learning future prediction timelines using Meta Prophet.",
    theme="soft"
)

app.launch(inline=True, debug=True)