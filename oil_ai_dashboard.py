# 1. CLEANING FUNCTION FOR YOUR CSVs
# -----------------------------------------------------------------
def clean_weekly_csv(file_path, price_column_name):
    print(f"\nProcessing file: {file_path}")
    if not os.path.exists(file_path):
        print(f"File not found: {file_path}")
        empty_df = pd.DataFrame(columns=['ds', price_column_name])
        empty_df['ds'] = pd.to_datetime(empty_df['ds']) # Ensure datetime dtype
        empty_df[price_column_name] = pd.to_numeric(empty_df[price_column_name]) # Ensure numeric dtype
        return empty_df

    df = pd.read_csv(file_path, encoding='latin1')
    df.columns = df.columns.str.strip()
    df = df.dropna(subset=['Week Of'])

    day_mapping = {
        'Mon': 0, 'Teu': 1, 'wed': 2, 'Wend': 2, 'Thu': 3, 'Fri': 4, 'fri': 4,
        'Tue': 1, 'Wed': 2, 'Thur': 3, 'Tues': 1 # Added more common variations for robustness
    }
    # Normalize DataFrame column names for case-insensitive lookup
    normalized_df_columns = {col.lower(): col for col in df.columns}

    rows = []
    if not df.empty: # Only iterate if df is not empty after initial cleaning
        for idx, row in df.iterrows():
            week_of_val = str(row['Week Of']).replace('\xa0', '').strip()

            # Flexible parsing for 'Week Of' string: if 'to' in week_of_val, split, otherwise use the whole string
            if 'to' in week_of_val:
                week_str = week_of_val.split('to')[0].strip()
            else:
                week_str = week_of_val

            # Debug print: What is the raw week string we are trying to parse?
            print(f"  Row {idx}: Processing 'Week Of': '{week_of_val}' -> parsed week_str: '{week_str}'")

            date_formats_to_try = [
                '%Y %b-%e',  # e.g., 2024 Jan-1
                '%Y %b %d',  # e.g., 2024 Jan 01
                '%Y-%m-%d',  # e.g., 2024-01-01
                '%d/%m/%Y',  # e.g., 01/01/2024
                '%m/%d/%Y',  # e.g., 01/01/2024
                '%Y-%b-%d',  # e.g., 2024-Jan-01
                '%Y-%m-%dT%H:%M:%S', # For ISO format if needed
                '%Y/%m/%d',
                '%b-%e-%Y', # e.g., Jan-1-2024
                '%b %e, %Y' # e.g., Jan 1, 2024
            ]
            base_date = pd.NaT
            for fmt in date_formats_to_try:
                base_date = pd.to_datetime(week_str, format=fmt, errors='coerce')
                if pd.notna(base_date):
                    print(f"    Successfully parsed week_str '{week_str}' with format '{fmt}' to base_date: {base_date}")
                    break

            if pd.isna(base_date):
                print(f"    Trying generic parse for week_str: '{week_str}'")
                base_date = pd.to_datetime(week_str, errors='coerce') # Fallback to generic parser

            if pd.isna(base_date):
                print(f"    Failed to parse base_date for week_str: '{week_str}'. Skipping row.")
                continue # Skip row if date parsing still fails

            for day_col_map, day_offset in day_mapping.items():
                # Find the actual column name in df (case-insensitive match)
                actual_day_col = normalized_df_columns.get(day_col_map.lower())

                if actual_day_col and pd.notna(row[actual_day_col]):
                    val = str(row[actual_day_col]).strip()
                    if val and val != '.' and val != '':
                        try:
                            actual_date = base_date + pd.Timedelta(days=day_offset)
                            rows.append({'ds': actual_date, price_column_name: float(val)})
                            print(f"      Added: ds={actual_date}, {price_column_name}={float(val)}")
                        except ValueError:
                            print(f"      Value error for '{val}' in column '{actual_day_col}'. Skipping.")
                            pass

    # If no rows were successfully extracted, return an empty DataFrame with the expected columns and dtypes
    if not rows:
        print(f"  No data successfully extracted for {file_path}. Returning empty DataFrame.")
        empty_df = pd.DataFrame(columns=['ds', price_column_name])
        empty_df['ds'] = pd.to_datetime(empty_df['ds']) # Ensure datetime dtype
        empty_df[price_column_name] = pd.to_numeric(empty_df[price_column_name]) # Ensure numeric dtype
        return empty_df

    cleaned_df = pd.DataFrame(rows)
    print(f"  Successfully extracted {len(cleaned_df)} rows for {file_path}.")
    return cleaned_df.drop_duplicates(subset=['ds'])

